        public async Task<IActionResult> Create([Bind("ReservaId,UserId,TipoCanchaId,EquipoId,FechaReserva,Hora,Estado")] Models.Reserva reserva)
        {
            if (ModelState.IsValid)
            {
                using (var cl = new HttpClient())
